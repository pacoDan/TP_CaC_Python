x=0
print("Ejemplo con while:")

while x<101:
    print(x)
    x=x+1

print("Ejemplo range() con un parametro:")
for x in range(101):
    print(x)

print("Ejemplo range() con dos parametros:")
for x in range(20,35):
    print(x)

print("Ejemplo range() con tres parametros")
for x in range(1,100,2):
    print(x)